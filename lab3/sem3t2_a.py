import turtle as tr

tr.shape('turtle')
tr.speed()
tr.color('blue')
tr.width(3)

tr.penup()
tr.goto(-300, 50)
tr.pendown()


c0 = [(180, 80), (90, 40), (90, 80), (90, 40), (3, 180, 50)]
c1 = [(3, -90, 40), (-90, 80), (180, 80), (135, 56.57), (0, -56.57), (3, -225, 10)]
c2 = [(-90, 40), (-90, 40), (-45, 56.57), (135, 40), (0, -40), (45, 56.57), (45, 40), (3, -90, 10)]
c3 = [(-90, 40), (-135, 56.57), (-45, -40), (45, 56.57), (0, -56.57), (3, -135, 40), (3, -90, 10)]
c4 = [(180, 40), (90, 40), (-90, 40), (0, -80), (3, 90, 10)]
c5 = [(3, -90, 40), (0, -40), (-90, 40), (90, 40), (-90, 40), (-90, 40), (0, -40), (3, -90, 80), (3, -90, 10)]
c6 = [(3, -90, 40), (-135, 56.57), (45, 40), (90, 40), (90, 40), (90, 40), (-135, 56.57), (3, -45, 10)]
c7 = [(-90, 40), (-135, 56.57), (45, 40), (3, 90, 40), (3, 90, 80), (3, -90, 10)]
c8 = [(180, 80), (90, 40), (90, 40), (90, 40), (0, -40), (-90, 40), (90, 40), (3, 180, 50)]
c9 = [(-90, 40), (-90, 40), (-45, 56.57), (0, -56.57), (-45, 40), (-90, 40), (3, -90, 50)]

c = (c0, c1, c2, c3, c4, c5, c6, c7, c8, c9)

def let(c):
    tr.setheading(90)
    for i in c:
        if i[0] == 3:
            tr.penup()
            tr.left(i[1])
            tr.forward(i[2])
        else:
            tr.pendown()
            tr.left(i[0])
            tr.forward(i[1])

def write(x):
    x = str(x)
    l = list(x)
    l = [int(i) for i in l]
    for i in l:
        let(c[i])


write(141700)


